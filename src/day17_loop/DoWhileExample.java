package day17_loop;

public class DoWhileExample {
    public static void main(String[] args) {


      //DO WHILE LOOP
        int n = 1;
        do{
            System.out.println("do while " + n);
            n++;
        }while (n<=10);

        //flow
        //1 - execute code block
        //2 - check condition

        //WHILE LOOP
        int m = 1;
        while ((m<=10)){
            System.out.println("while " + m);
         m++;
        }
        //flow
        //1-check condition
        //2-if true execute the code block
    }
}
