package day04_variables;

public class PrintDate {
    public static void main(String[] args) {
        //declare and assign 3 variables -- month number,day number,year number
        //
       int month = 10;
       int day = 22;
       int year = 2024;

        System.out.println(month + "/" +day + "/" + year);
        System.out.println(month+day+year);
        System.out.println(month + "-" +day + "-" + year);
        System.out.println(month + "|" +day + "|" + year);
    }
}