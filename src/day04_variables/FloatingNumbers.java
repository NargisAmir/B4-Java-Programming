package day04_variables;

public class FloatingNumbers {
    public static void main(String[] args) {
        System.out.println(3);
        System.out.println(4);

        System.out.println(3 + 4);
        System.out.println(3 * 4);
        //in java we can not store the fraction with int data type
        System.out.println(3/4); //0.75 int --whole numvber portion ---0

        System.out.println(13/4);// 3.25--- int --- whole number portion---3
        //that is why we have floating data types
        double price = 10.99;//this is the way to store decimal numbers with fractions
        float gpa = 3.4f;

    }
}
